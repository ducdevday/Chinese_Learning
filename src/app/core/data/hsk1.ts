/**
 * Giáo trình chuẩn HSK 1 — 15 bài, ~150 từ vựng.
 * File nghe đặt trong public/audio/hsk1, tên "BB-T.mp3" (BB = số bài, T = số track).
 * Quy ước track của giáo trình: track 1–3 là các hội thoại (课文),
 * các track giữa là luyện phát âm/ngữ âm, track cuối là bài tập nghe.
 */
function tracks(lesson: number, count: number): AudioTrack[] {
  const nn = String(lesson).padStart(2, '0');
  const list: AudioTrack[] = [];
  for (let t = 1; t <= count; t++) {
    let label: string;
    if (t <= 3 && count > 3) label = `Hội thoại ${t}`;
    else if (t <= 3 && count <= 3) label = `Hội thoại ${t}`;
    else if (t === count) label = 'Bài tập nghe';
    else label = `Luyện phát âm ${t - 3}`;
    list.push({ label, file: `audio/hsk1/${nn}-${t}.mp3` });
  }
  return list;
}

